<p><b>&mdash; Прежде всего, хотела бы поздравить вас с&nbsp;премьерой новой части «Матрицы». Каково вам было вернуться к&nbsp;своим ролям &mdash; Нео и Тринити &mdash; спустя 18&nbsp;лет?</b>
</p>
<div class="_s_banner_adcenter" aria-hidden="true"></div>
<p><b>Киану Ривз:</b> Просто невероятно и в&nbsp;то же время неожиданно. Возвращение к&nbsp;роли, возможность снова принять участие в&nbsp;этой истории спустя столько лет &mdash; уникальное стечение обстоятельств, такое редко случается. Мне нравится мой герой и сам замысел, так что я был счастлив снова сыграть Нео.
</p>
<p><b>Кэрри-Энн Мосс:</b> Да, это было чудесно. Съемки продолжения, возвращение к&nbsp;тренировкам, работе с&nbsp;Киану, Ланой и новыми актерами &mdash; все это было очень волнительно для&nbsp;меня. Могу сказать, что каждый наш съемочный день был полон креатива, все члены команды находились в&nbsp;предчувствии чего-то удивительного. И это было правда здорово.
</p>
<p><b>&mdash; &laquo;Матрицу: Воскрешение&raquo; сняла Лана Вачовски, без&nbsp;участия Лилли. Сказалось ли это как-то на&nbsp;проекте?</b>
</p>
<p><b>Ривз:</b> Мы скучали по&nbsp;работе с&nbsp;Лилли. В&nbsp;этот раз Лана объединилась с&nbsp;<a href="/tags/person/dzheims_maktig.shtml" class="tag">Джеймсом Мактигом</a> (продюсер фильма &mdash; &laquo;Газета.Ru&raquo;), который выполнял роль первого помощника режиссера при&nbsp;работе с&nbsp;ней над&nbsp;&laquo;Восьмым чувством&raquo;. Можно сказать, что он в&nbsp;некотором смысле выступал соратником Ланы. Причем иногда Джеймс даже брал на&nbsp;себя функции режиссера. В&nbsp;общем, работать с&nbsp;Ланой было здорово, это прекрасный опыт. Но и по&nbsp;Лилли мы очень скучали.
</p>
<div aria-hidden="true" class="_s_banner_inread" ></div>
<p><b>&mdash; Если бы вам, настоящим Киану Ривзу и Кэрри-Энн Мосс, пришлось выбирать между&nbsp;идеальной жизнью в&nbsp;матрице и реальностью, какую таблетку бы взяли &mdash; синюю или красную? И почему?</b>
</p>
<p><b>Ривз:</b> Я бы выбрал красную таблетку. Хотя, конечно, тут много различных факторов, которые надо брать в&nbsp;расчет. Но я все равно придерживаюсь идеи «реального опыта» (<i>показывает кавычки пальцами</i>). Для&nbsp;меня это все-таки ценнее всего.
</p>
<p><b>Мосс:</b> Для&nbsp;меня то же самое. Я тоже выбираю красную таблетку. Правда, должна признаться, что ни разу не слышала формулировку, в&nbsp;которой на&nbsp;выбор предлагали «синюю таблетку с&nbsp;идеальной жизнью». Ведь в&nbsp;этом случае будет гораздо сложнее ответить. Но все равно &mdash; выбираю красную!
</p>
<p><b>Ривз:</b> А какую выбираешь ты (<i>смеется</i>)?
</p>
<p><b>&mdash; Тоже красную! А что касается конкретных сцен нового фильма &mdash; какая у&nbsp;вас самая любимая?</b>
</p>
<p><b>Ривз:</b> Здесь сложно ответить, при&nbsp;этом не раскрыв что-то важное из&nbsp;сюжета (<i>смеется</i>). Скажем так, есть одна сцена с&nbsp;Тринити и Нео / <a href="/tags/person/tomas_anderson.shtml" class="tag">Томасом Андерсоном</a>, но она уже ближе к&nbsp;концу фильма. Вот именно она &mdash; одна из&nbsp;самых любимых.
</p>
<p><b>Мосс:</b> А для&nbsp;меня это сцена в&nbsp;зале дзюдо с&nbsp;персонажами Киану и Яхьи. В&nbsp;этот момент Нео проявляет свою силу и все ранее подавляемые чувства. Как бы ее еще описать, чтобы не выдать слишком много...
</p>
<p><b>Ривз:</b> В&nbsp;общем, это сцена, в&nbsp;которой Нео показывает, насколько сильны его чувства к&nbsp;Тринити. Во время борьбы с&nbsp;Морфеусом там показано внутреннее противостояние моего героя.
</p>
<p><b>Мосс:</b> Да, меня особенно тронул этот момент.
</p>
