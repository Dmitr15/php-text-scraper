<p><b>&mdash; Яхья, Джессика, вы отлично сыграли в&nbsp;тандеме. Когда вы получили роли в&nbsp;новой «Матрице», какие эмоции испытали?</b>
</p>
<p><b>Яхья Абдул-Матин II:</b> Я сразу позвонил своей матери. Казалось, что все это неправда, мне не верилось. Это был шок. И потом, помню, меня вытащили отпраздновать это событие. Хотя я тогда был довольно занят с&nbsp;прессой, работал над&nbsp;кое-чем. И вот один из&nbsp;друзей сказал: «Ты сошел с&nbsp;ума? А ну-ка вставай &mdash; и пошли праздновать. Сейчас возьмем выпить». В&nbsp;общем, вечер выдался на&nbsp;славу!
</p>
<p><b>Джессика Хенвик:</b> А я вот не помню, чтобы поехала куда-то отмечать. Да, звучит странно, но на&nbsp;момент заветного звонка находилась в&nbsp;<a href="/tags/geo/los-andzheles.shtml" class="tag">Лос-Анджелесе</a>. И буквально на&nbsp;следующий день мне пришлось лететь обратно в&nbsp;Англию &mdash; переваривала утверждение в&nbsp;проект в&nbsp;воздухе. И до&nbsp;сих пор все как в&nbsp;тумане.
</p>
<p><b>&mdash; Каково было работать c Ланой Вачовски?</b>
</p>
<p><b>Абдул-Матин II:</b> О, это было круто! Ни один съемочный день не был похож на&nbsp;предыдущий. Лана относится к&nbsp;тем режиссерам-самоучкам, которые познавали все на&nbsp;практике. У&nbsp;нее нестандартное мышление, она умеет собирать команду. Лану вдохновляют люди и события, поэтому порой все может измениться в&nbsp;последнюю минуту &mdash; в&nbsp;общем, рабочий процесс был похож на&nbsp;игру. А еще она очень умная, ей важен интеллект актеров, которых она приглашает в&nbsp;фильм. Это важно, чтобы поддерживать креативный и интеллектуальный дух на&nbsp;съемках, что, в&nbsp;свою очередь, отражается на&nbsp;атмосфере фильма. Лана &mdash; творческий гений, подобных которому я ранее не встречал.
</p>
<p><b>&mdash; В&nbsp;«Матрице» очень много экшна, перестрелок, гонок. При&nbsp;этом трюки вам приходилось исполнять самостоятельно &mdash; как это было?</b>
</p>
<p><b>Хенвик:</b> Превозмогая пот и боль. Я уже снималась в&nbsp;ряде фильмов, где были экшн-сцены. Но в&nbsp;«Матрице» новым опытом для&nbsp;меня стало использование оружия &mdash; до&nbsp;этого мне ни разу не приходилось брать в&nbsp;руки пистолеты, да еще и стрелять из&nbsp;них на&nbsp;съемочной площадке. Они оказались гораздо тяжелее, чем я ожидала. К&nbsp;тому же у&nbsp;них сильная отдача, поэтому было довольно сложно стрелять так, чтобы рука не дрожала. Так что я многому научилась за&nbsp;время съемок.
</p>
<p><b>Абдул-Матин II: </b>Наш специалист по&nbsp;оружейному реквизиту Роберт Галотти &mdash; просто потрясающий, настоящий мастер своего дела. Безопасность для&nbsp;него превыше всего, Роб лично следил за&nbsp;качеством наших тренировок, поэтому мы были идеально подготовлены к&nbsp;любым ситуациям. О&nbsp;нас очень хорошо позаботились.
</p>
<p><b>&mdash; Что вам больше всего нравится в&nbsp;ваших героях &mdash; и как готовились к&nbsp;их воплощению на&nbsp;экране?</b>
</p>
<p><b>Абдул-Матин II:</b> То, что он осознает себя. Ему известно об истории появления матрицы, а также о&nbsp;личности Морфеуса. А еще мой герой слегка нахальный и умеет разрядить обстановку. К&nbsp;тому же он не подражает, а дает свою интерпретацию Морфеуса, что мне как актеру очень близко. Это меня привлекло в&nbsp;нем, когда я впервые прочел сценарий. Что же касается моей подготовки к&nbsp;роли, она заключалась в&nbsp;глубоком погружении в&nbsp;мир &laquo;Матрицы&raquo;. Я представлял себе историю, но при&nbsp;этом старался привнести в&nbsp;нее что-то новое.
</p>
<p><b>Хенвик:</b> Моя героиня &mdash; очень отважная. И хотя она не находится в&nbsp;матрице, все равно никого не слушает, продолжая верить в&nbsp;нечто, за&nbsp;что окружающие считают ее сумасшедшей. Она настолько решительная и уверенная, что остается верной себе, несмотря на&nbsp;мнения коллег и вышестоящих сослуживцев. Я бы хотела быть такой же сильной, как она.
</p>
