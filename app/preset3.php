<p><b>&mdash; Какой была ваша первая реакция, когда вы узнали о&nbsp;том, что будете играть в&nbsp;продолжении &laquo;Матрицы&raquo;?</b>
</p>
<p>&mdash; Как только стало известно о&nbsp;запуске новой «Матрицы», вокруг было столько воодушевленных дискуссий... Правда, сама я на&nbsp;тот момент находилась в&nbsp;<a href="/tags/geo/mumbai.shtml" class="tag">Мумбаи</a>. И вот мне по&nbsp;телефону сообщили о&nbsp;том, что Лана Вачовски хочет со мной встретиться. Я взяла билет в&nbsp;<a href="/tags/geo/san-frantsisko.shtml" class="tag">Сан-Франциско</a>, и потом мы с&nbsp;ней разговаривали около&nbsp;часа &mdash; а затем она вручила мне свеженапечатанный сценарий нового фильма! Быть частью команды в&nbsp;«Матрице» &mdash; уже сама по&nbsp;себе большая честь, но для&nbsp;меня это еще и ответственность перед&nbsp;наследием, поскольку мне досталась роль девочки Сати из&nbsp;предыдущих частей. Это был потрясающий опыт также по&nbsp;той причине, что роль моей героини является связующим звеном в&nbsp;сюжете.
</p>
<p><b>&mdash; Каково было работать в&nbsp;Ланой Вачовски? Все-таки в&nbsp;этот раз она снимала сольно, без&nbsp;своей сестры Лилли&hellip;</b>
</p>
<p>&mdash; Поскольку я знакома только с&nbsp;Ланой Вачовски, могу сказать про&nbsp;нее, что она довольно щепетильный режиссер. Именно поэтому Лана и создала столь необычный, но продуманный до&nbsp;мелочей мир. При&nbsp;этом она точно знает, что хочет, у&nbsp;нее есть четкое видение развития событий. И я всецело полагалась на&nbsp;нее во всем.
</p>
<p><b>&mdash; Расскажите, как вы готовились к&nbsp;роли. Что вам больше всего понравилось в&nbsp;вашей героине и ее истории? </b>
</p>
<p>&mdash; У&nbsp;меня ушло много времени на&nbsp;подготовку к&nbsp;роли. В&nbsp;особенности на&nbsp;то, чтобы понять, откуда пришла Сати &mdash; и почему она оказалась в&nbsp;этом месте. Мне важно было понять, какова ее функция. Все эти моменты были несколько раз упомянуты в&nbsp;предыдущих частях, были намеки на&nbsp;то, кем она станет в&nbsp;будущем. Так что было очень прикольно сыграть повзрослевшую версию той самой девочки. И еще больше меня волновал вопрос, какая у&nbsp;нее мотивация в&nbsp;мире людей и машин, поскольку Сати олицетворяет единение и любовь. Пожалуй, это и привлекло меня больше всего &mdash; желание объединить эти миры. А в&nbsp;самой истории мне понравилось то, что, по&nbsp;сути, четвертая часть обыгрывает первую «Матрицы», в&nbsp;которой воспевается любовь Нео и Тринити.
</p>
<p><b>&mdash; Какая сцена в&nbsp;фильме у&nbsp;вас самая любимая?</b>
</p>
<p>&mdash; Я бы сказала, что финальная сцена. Было много моментов, но концовка меня тронула до&nbsp;глубины души.
